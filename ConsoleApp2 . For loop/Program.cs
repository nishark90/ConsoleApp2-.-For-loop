using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp2.For_loop
{
    internal class Program
    {
        static void Main(string[] args)
            // print 1 to 10//
        { int i;
            for (i = 1; i <= 10; i++)
            {
                Console.WriteLine(i);
            
            }
            Console.ReadLine();
        }
    }
}
